import * as fs from 'fs';
import * as path from 'path';

const INPUT_PATH = path.resolve(__dirname, '../assets/index/index.html');
const OUTPUT_PATH = path.resolve(__dirname, '../../src/publish/siteAssets.ts');

if (!fs.existsSync(INPUT_PATH)) {
	console.error(`Source file not found: ${INPUT_PATH}`);
	process.exit(1);
}

const html = fs.readFileSync(INPUT_PATH, 'utf-8');

// Extract CSS: everything between <style> and </style>
const styleMatch = html.match(/<style>([\s\S]*?)<\/style>/);
if (!styleMatch) {
	console.error('Could not find <style> block in source HTML');
	process.exit(1);
}
const css = styleMatch[1].trim();

// Extract FlexSearch: the first <script> block containing "FlexSearch"
const flexSearchMatch = html.match(/<script>\s*(\/\*\*!\s*\n\s*\*\s*FlexSearch[\s\S]*?)\s*<\/script>/);
if (!flexSearchMatch) {
	console.error('Could not find FlexSearch <script> block in source HTML');
	process.exit(1);
}
const flexSearchJs = flexSearchMatch[1].trim();

// Extract the vendored diff libraries (jsdiff + diff2html): the <script> block
// led by the `/*! CPN diff vendor` marker comment. Kept in its own block (with
// its own real </script>) so the app-script extractor below stops at the right
// boundary and a future lib update containing a stray </script> can't silently
// truncate app.js.
const vendorMatch = html.match(/<script>\s*(\/\*! CPN diff vendor[\s\S]*?)\s*<\/script>/);
if (!vendorMatch) {
	console.error('Could not find vendored diff-library <script> block in source HTML');
	process.exit(1);
}
const vendorJs = vendorMatch[1].trim();

// Extract app JS: the first <script> block AFTER both the FlexSearch and vendor
// blocks. Use the later of the two end offsets so the app extractor is order-
// independent (it doesn't matter whether vendor precedes or follows FlexSearch).
const flexSearchEnd = html.indexOf('</script>', html.indexOf(flexSearchMatch[0])) + '</script>'.length;
const vendorEnd = html.indexOf('</script>', html.indexOf(vendorMatch[0])) + '</script>'.length;
const remainingHtml = html.slice(Math.max(flexSearchEnd, vendorEnd));
const appScriptMatch = remainingHtml.match(/<script>([\s\S]*?)<\/script>/);
if (!appScriptMatch) {
	console.error('Could not find application <script> block in source HTML');
	process.exit(1);
}
const appJs = appScriptMatch[1].trim();

// Build HTML shell template
const CONFIG_BOOTSTRAP = `fetch('config.json')
  .then(r => r.ok ? r.json() : null)
  .then(config => {
    if (!config) return;
    // Expose parsed config for the app (e.g. the comment client reads the
    // comment endpoints + auth login URL from here). Mirrors window.__CPN_HOME_UID__.
    window.__CPN_CONFIG__ = config;
    const root = document.documentElement;
    if (config.fontFamily) document.body.style.fontFamily = config.fontFamily;
    function applyTheme() {
      const theme = root.getAttribute('data-theme');
      const isDark = theme === 'dark' || (!theme && window.matchMedia('(prefers-color-scheme: dark)').matches);
      const overrides = isDark ? config.theme?.dark : config.theme?.light;
      if (overrides) {
        Object.entries(overrides).forEach(([key, val]) => root.style.setProperty('--' + key, val));
      }
    }
    applyTheme();
    new MutationObserver(applyTheme).observe(root, { attributes: true, attributeFilter: ['data-theme'] });
  })
  .catch(() => {});`;

const indexTemplate = `<!DOCTYPE html>
<html lang="en">
<head>
\t<meta charset="UTF-8">
\t<meta name="viewport" content="width=device-width, initial-scale=1.0">
\t<link rel="icon" href="data:image/x-icon;base64,">
\t<title>{{SITE_TITLE}}</title>
\t<link rel="stylesheet" href="styles.css">
</head>
<body>
\t<header class="header">
\t\t<div class="header-left">
\t\t\t{{HEADER_LINKS_HTML}}
\t\t</div>
\t\t<div class="header-right">
\t\t\t<div class="search-container">
\t\t\t\t<input type="text" id="search-input" placeholder="Search notes..." aria-label="Search notes">
\t\t\t\t<div id="search-results" class="search-results"></div>
\t\t\t</div>
\t\t\t<button id="theme-toggle" class="theme-toggle" aria-label="Toggle dark mode" title="Toggle dark mode">
\t\t\t\t<span class="theme-icon">🌙</span>
\t\t\t</button>
\t\t</div>
\t</header>

\t<div class="panels-container" id="panels"></div>

\t<script>${CONFIG_BOOTSTRAP}</script>
\t{{HOME_NOTE_UID_SCRIPT}}
\t<script src="flexsearch.min.js"></script>
\t<script src="vendor.js"></script>
\t<script src="app.js"></script>
</body>
</html>`;

// Write output
const output = `// Auto-generated by infrastructure/bin/synth-site-assets.ts — do not edit manually.
// Regenerate with: npm run synth:site

export const SITE_INDEX_TEMPLATE = ${JSON.stringify(indexTemplate)};

export const SITE_STYLES_CSS = ${JSON.stringify(css)};

export const SITE_APP_JS = ${JSON.stringify(appJs)};

export const FLEXSEARCH_MIN_JS = ${JSON.stringify(flexSearchJs)};

export const VENDOR_JS = ${JSON.stringify(vendorJs)};
`;

fs.writeFileSync(OUTPUT_PATH, output, 'utf-8');
console.log(`Site assets written to ${OUTPUT_PATH}`);
console.log(`  Index template: ${(indexTemplate.length / 1024).toFixed(1)} KB`);
console.log(`  Styles CSS: ${(css.length / 1024).toFixed(1)} KB`);
console.log(`  App JS: ${(appJs.length / 1024).toFixed(1)} KB`);
console.log(`  FlexSearch JS: ${(flexSearchJs.length / 1024).toFixed(1)} KB`);
console.log(`  Vendor JS (jsdiff + diff2html): ${(vendorJs.length / 1024).toFixed(1)} KB`);
