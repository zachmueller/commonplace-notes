const assembly = app.synth();

function stripCdkMetadata(template: Record<string, any>): Record<string, any> {
	// Remove CDKMetadata resource
	if (template.Resources) {
		delete template.Resources.CDKMetadata;
	}

	// Remove BootstrapVersion parameter
	if (template.Parameters?.BootstrapVersion) {
		delete template.Parameters.BootstrapVersion;
		if (Object.keys(template.Parameters).length === 0) {
			delete template.Parameters;
		}
	}

	// Remove CheckBootstrapVersion rule
	if (template.Rules?.CheckBootstrapVersion) {
		delete template.Rules.CheckBootstrapVersion;
		if (Object.keys(template.Rules).length === 0) {
			delete template.Rules;
		}
	}

	// Remove CDK path metadata from all resources
	if (template.Resources) {
		for (const resource of Object.values(template.Resources) as any[]) {
			if (resource.Metadata) {
				delete resource.Metadata['aws:cdk:path'];
				if (Object.keys(resource.Metadata).length === 0) {
					delete resource.Metadata;
				}
			}
		}
	}

	// Remove Conditions that reference BootstrapVersion
	if (template.Conditions) {
		for (const [key, value] of Object.entries(template.Conditions)) {
			const condStr = JSON.stringify(value);
			if (condStr.includes('BootstrapVersion')) {
				delete template.Conditions[key];
			}
		}
		if (Object.keys(template.Conditions).length === 0) {
			delete template.Conditions;
		}
	}

	return template;
}

function getTemplate(stackName: string): string {
	const stackArtifact = assembly.getStackByName(stackName);
	const templatePath = path.join(assembly.directory, stackArtifact.templateFile);
	const template = JSON.parse(fs.readFileSync(templatePath, 'utf-8'));
	const cleaned = stripCdkMetadata(template);
	return JSON.stringify(cleaned);
}

const bootstrapTemplate = getTemplate('CpnBootstrapStack');
const certTemplate = getTemplate('CpnCertificateStack');
const cognitoTemplate = getTemplate('CpnCognitoAuthStack');
const passwordTemplate = getTemplate('CpnPasswordAuthStack');
const oacTemplate = getTemplate('CpnFullStackOac');
const oaiTemplate = getTemplate('CpnFullStackOai');
const commentTemplate = getTemplate('CpnCommentStack');

// The password edge fn is packaged as an S3 asset, not inline. The plugin bakes
// a `const CFG = {...}` line in front of this compacted body and zips the
// result, so ship the verbatim (comment-stripped) body as a constant. Uses the
// same compaction as the old inline path, so behavior is byte-for-byte identical
// once CFG is prepended.
const passwordEdgeBody = compactLambdaSource('password-edge.js');

const outputPath = path.resolve(__dirname, '../../src/infrastructure/templates.ts');

const output = `// Auto-generated by infrastructure/bin/synth.ts — do not edit manually.
// Regenerate with: npm run synth

export const BOOTSTRAP_TEMPLATE = ${JSON.stringify(bootstrapTemplate)};

export const CERTIFICATE_TEMPLATE = ${JSON.stringify(certTemplate)};

export const COGNITO_AUTH_TEMPLATE = ${JSON.stringify(cognitoTemplate)};

export const PASSWORD_AUTH_TEMPLATE = ${JSON.stringify(passwordTemplate)};

export const PASSWORD_EDGE_BODY = ${JSON.stringify(passwordEdgeBody)};

export const FULL_STACK_OAC_TEMPLATE = ${JSON.stringify(oacTemplate)};

export const FULL_STACK_OAI_TEMPLATE = ${JSON.stringify(oaiTemplate)};

export const COMMENT_STACK_TEMPLATE = ${JSON.stringify(commentTemplate)};
`;

fs.writeFileSync(outputPath, output, 'utf-8');
